//Ciclo whit for
console.log('=== FOR ===');
for (let i = 0;i < 5;i++){
  console.log(`Repeticion numero ${i}`);
}

//Ciclo whit while
console.log('=== WHILE ===');
let a = 0;
while (a < 5) {
  console.log(`while: ${a}`);
  a++;
}

//Ciclo whit do while
console.log('=== DO WHILE ===');
let j = 0;
do {
  console.log(`do while: ${j}`);
  j++;
} while (j < 5);